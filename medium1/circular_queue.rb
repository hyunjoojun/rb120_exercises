class CircularQueue
  def initialize(max_size)
    @queue = []
    @max_size = max_size
  end

  def enqueue(obj)
    dequeue if full?
    queue << obj
  end

  def dequeue
    queue.shift
  end

  private

  attr_reader :queue, :max_size

  def full?
    queue.size == max_size
  end
end

queue = CircularQueue.new(3)
puts queue.dequeue == nil

queue.enqueue(1)
queue.enqueue(2)
puts queue.dequeue == 1

queue.enqueue(3)
